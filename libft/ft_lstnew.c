#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*elem;

	elem = malloc(sizeof(t_list));
	if (elem == 0)
		return (NULL);
	elem->content = content;
	elem->next = NULL;
	return (elem);
}
